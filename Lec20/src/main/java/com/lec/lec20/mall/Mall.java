package com.lec.lec20.mall;

public class Mall {
	
	private String gender;
	private boolean cookieDel;
	private boolean sessionDel;
	
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public boolean isCookieDel() {
		return cookieDel;
	}
	public void setCookieDel(boolean cookieDel) {
		this.cookieDel = cookieDel;
	}
	public boolean isSessionDel() {
		return sessionDel;
	}
	public void setSessionDel(boolean sessionDel) {
		this.sessionDel = sessionDel;
	}
	
}